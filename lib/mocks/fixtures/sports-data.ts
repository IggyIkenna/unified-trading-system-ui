function isLocked(bm: Bookmaker): boolean {
  return !SUBSCRIBED_BOOKMAKERS.includes(bm);
}

// Generate 30-second interval progressive snapshots for completed match replay
function generateProgressiveSnapshots(durationMin: number): {
  stats: ProgressiveStatsSnapshot[];
  odds: ProgressiveOddsSnapshot[];
} {
  const steps = Math.floor((durationMin * 60) / 30);
  const stats: ProgressiveStatsSnapshot[] = [];
  const odds: ProgressiveOddsSnapshot[] = [];

  for (let i = 0; i <= steps; i++) {
    const totalSeconds = i * 30;
    const min = Math.floor(totalSeconds / 60);
    const sec = totalSeconds % 60;
    const timer = `${String(min).padStart(2, "0")}:${String(sec).padStart(2, "0")}`;
    const minuteDecimal = totalSeconds / 60;
    const t = minuteDecimal / durationMin; // 0-1 progress

    // Stats grow progressively through match
    stats.push({
      timer,
      minuteDecimal,
      home: {
        xg: parseFloat((0.8 * t + Math.sin(t * 5) * 0.1).toFixed(2)),
        shotsTotal: Math.round(8 * t + Math.sin(t * 4) * 1),
        shotsOnTarget: Math.round(4 * t),
        possession: Math.round(48 + Math.sin(t * 3) * 6),
        corners: Math.round(4 * t),
        fouls: Math.round(7 * t),
        yellowCards: t > 0.5 ? 1 : 0,
        redCards: 0,
        dangerousAttacks: Math.round(35 * t),
      },
      away: {
        xg: parseFloat((1.1 * t + Math.cos(t * 5) * 0.1).toFixed(2)),
        shotsTotal: Math.round(11 * t + Math.cos(t * 4) * 1),
        shotsOnTarget: Math.round(5 * t),
        possession: Math.round(52 - Math.sin(t * 3) * 6),
        corners: Math.round(6 * t),
        fouls: Math.round(9 * t),
        yellowCards: t > 0.6 ? 2 : t > 0.3 ? 1 : 0,
        redCards: 0,
        dangerousAttacks: Math.round(45 * t),
      },
    });

    // Odds shift as match progresses — away team had the goal at ~35min
    const homeWin = t < 0.3 ? 2.4 - t * 0.5 : t < 0.5 ? 3.1 + t * 0.8 : 3.8 - t * 0.3;
    const awayWin = t < 0.3 ? 3.0 - t * 0.3 : t < 0.5 ? 2.0 - t * 0.2 : 1.8 - t * 0.1;
    const draw = t < 0.5 ? 3.4 + t * 0.2 : 4.2 - t * 0.5;

    odds.push({
      timer,
      minuteDecimal,
      odds1x2: {
        home: parseFloat(homeWin.toFixed(2)),
        draw: parseFloat(draw.toFixed(2)),
        away: parseFloat(awayWin.toFixed(2)),
      },
      ouLine: 2.5,
      ouOver: t < 0.5 ? 1.85 - t * 0.2 : 1.55 + t * 0.4,
      ouUnder: t < 0.5 ? 2.0 + t * 0.3 : 2.5 - t * 0.5,
    });
  }

  return { stats, odds };
}

