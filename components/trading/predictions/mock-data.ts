// ─── Price series generation ──────────────────────────────────────────────────

function makePriceSeries(startProb: number, endProb: number, n: number, seed: number) {
  const probs = generateSeries(n, startProb, endProb, 3, seed);
  const now = Date.now();
  return probs.map((prob, i) => ({
    t: Math.floor((now - (n - i) * 3600_000) / 1000),
    prob,
  }));
}

function makeUnderlyingSeries(
  startPrice: number,
  endPrice: number,
  startOdds: number,
  endOdds: number,
  n: number,
  seed: number
): UnderlyingDataPoint[] {
  const prices = generateSeries(n, startPrice, endPrice, startPrice * 0.005, seed);
  const odds = generateSeries(n, startOdds, endOdds, 3, seed + 1);
  const now = Date.now();
  return prices.map((price, i) => ({
    t: Math.floor((now - (n - i) * 900_000) / 1000), // 15m intervals
    price: Math.round(price * 100) / 100,
    oddsYes: Math.round(odds[i] * 10) / 10,
  }));
}

